fruits = ["apple", "pear", "orange", "banana", "kiwi"]

print(fruits[0]) 
# python is 0-indexed so the first element in the list is located at pos 0

print(fruits[0].upper()) 
# methods can be used on list elements as if they were anything else

print(fruits[-1])
# python has negative syntax for lists to access the last elements of a list
# [-1] is the last element, [-2] is the second to last, so on and so forth
